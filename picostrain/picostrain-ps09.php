				<div id="sub2full">
				<p>Like other members of the PICOSTRAIN product family PS09 supports the connection of only a half-bridge as sensor without any loss in resolution quality. However, unlike its ancestors the PS09 contains additionally an internal temperature unit which allows a convenient temperature compensation of half-bridge sensors without any additional external compensation resistors.  This way, the whole sensor gets simpler and the temperature compensation more convenient than ever before. Here again, especially the Digital Load Cell takes full advantage of this new feature:</p>
				
