        <h1 class="primary-heading">AS1382 Buck Converter</h1>
        <h2 class="secondary-heading">Small Solution 1A Step Down DC-DC Converter</h2>

        <p class="intro">
        The AS1382 is a high-efficiency, constant-frequency synchronous buck converter available in adjustable and fixed-voltage versions. The wide input voltage range (2.7V to 5.5V), automatic power save mode and minimal external component requirements make the AS1382 perfect for any single Li-Ion battery-powered application. Typical supply current with no load is 95&mu;A and decreases to 0.04&mu;A in shutdown mode.
        <br />
        An internal synchronous switch increases efficiency and eliminates the need for an external Schottky diode. The internally fixed switching frequency (2MHz, 3MHz or 4MHz) allows the usage of small surface mount external components.
        <br />
        Very low output voltages can be delivered with the internal 0.6V feedback reference voltage. The AS1382 is available in a 6-pin WL-CSP package.
